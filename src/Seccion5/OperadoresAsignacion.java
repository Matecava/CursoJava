package Seccion5;

public class OperadoresAsignacion {

    public static void main(String[] args) {
        int a = 3;
        
        a += 1;
        System.out.println("a = " + a);
        
        a += 3;
        System.out.println("a = " + a);
        
        a -= 1;
        System.out.println("a = " + a);
        
        a *= 10;
        System.out.println("a = " + a);
        
        a /= 4;
        System.out.println("a = " + a);
        
        a %= 5;
        System.out.println("a = " + a);
        
        
    }
}
